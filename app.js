function showTask() {
    let name = document.getElementById("name").value;
    let output = document.getElementById("output");

    if (!DATA[name]) {
        output.innerHTML = "";
        return;
    }

    let d = DATA[name];

    let html = `<div class="card">
        <h2>${d.title}</h2>
        <p><b>🎯 Objective:</b> ${d.objective}</p>
    `;

    d.steps.forEach(s => {
        html += `
        <div class="section">
            <h3>${s.step}</h3>
            <p>${s.content.replace(/\n/g, "<br>")}</p>
            <p class="check">✔ Checklist: ${s.check}</p>
        </div>
        `;
    });

    html += `</div>`;

    output.innerHTML = html;
}
